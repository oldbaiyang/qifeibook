# 转换为JS格式
def book_to_js(book):
    links_js = ",\n      ".join([
        f"{{\n        name: \"{link['name']}\",\n        url: \"{link['url']}\",\n        code: \"{link['code']}\"\n      }}"
        for link in book["downloadLinks"]
    ])
    
    desc = book['description'][:200].replace('"', '\\"')
    author_detail = book['authorDetail'].replace('"', '\\"')
    title = book['title'].replace('"', '\\"')
    author = book['author'].replace('"', '\\"')
    cover = book['cover'].replace('"', '\\"')
    
    js = f"""  {{
    id: {book['id']},
    title: "{title}",
    author: "{author}",
    authorDetail: "{author_detail}",
    year: "{book['year']}",
    cover: "{cover}",
    description: "{desc}...",
    category: "{book['category']}",
    downloadLinks: [
      {links_js}
    ],
    size: "{book['size']}",
    format: "{book['format']}",
    publishYear: "{book['publishYear']}"
  }}"""
    return js

# 生成新的书籍条目JS
